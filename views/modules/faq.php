<h2>Frequently Asked Questions</h2><br />

<p><strong>How do I get more searches?</strong><br>
Go to your management page and put the word out about us. For every new friend that joins, we will permanently give you 5 additional searches.
</p>

<p><strong>How do I manage my searches?</strong><br>
Look at your email. There is a management link at the bottom of every email with a special code to allow only you to manage your notifications. If you have previously clicked on the link, a cookie will be stored on that computer and a My Searches tab will appear.
</p>

<p><strong>How do I suggest an idea for a new feature?</strong><br>
Click the orange "Make a Suggestion" tab at the top and fill out the form that appears.
</p>

<p><strong>How can I easily search more than 1 location?</strong><br>
You can now enter multiple cities. Enter a location as you normally would and it will appear in a bubble. Continue adding up to 20 areas.
</p>

<p><strong>But how can I search several states easily?</strong><br>
We now allow statewide searches that will search all areas within that state.
</p>

<p><strong>What are some reasons I may be having trouble with the website loading correctly?</strong>
<ol>
<li>Try reloading the web page. We may have made an update that isn't in your browser's cache.</li>
<li>Try clearing your web browser's cache. If you're not sure how to do that, try searching the web and you should find instructions.</li>
<li>If that doesn't work, be sure your browser is running the latest version and that you have JavaScript enabled.</li>
</ol>
</p>